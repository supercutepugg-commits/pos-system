-- 1) 미리보기: 매칭 결과와 몇 건이 바뀌는지 확인 (DB 변경 없음)
SELECT fa.id, fa.business_name, fa.phone, fa.status AS current_status, s.status_key AS restore_to
FROM franchise_applications fa
JOIN _snapshot s ON regexp_replace(s.phone, '\D', '', 'g') = regexp_replace(fa.phone, '\D', '', 'g')
  AND (s.business_number = '' OR fa.business_number = s.business_number OR fa.business_number IS NULL)
WHERE fa.status = 'completed'
ORDER BY fa.business_name;

-- 2) 매칭 안 되는(스냅샷에 없거나 phone이 달라진) completed 건 확인
SELECT fa.id, fa.business_name, fa.owner_name, fa.phone
FROM franchise_applications fa
WHERE fa.status = 'completed'
AND NOT EXISTS (
  SELECT 1 FROM _snapshot s WHERE regexp_replace(s.phone, '\D', '', 'g') = regexp_replace(fa.phone, '\D', '', 'g')
);

-- 3) 위 1번 결과가 맞다고 판단되면 아래 UPDATE 실행 후 COMMIT
UPDATE franchise_applications fa
SET status = s.status_key, updated_at = now()
FROM _snapshot s
WHERE regexp_replace(s.phone, '\D', '', 'g') = regexp_replace(fa.phone, '\D', '', 'g')
  AND (s.business_number = '' OR fa.business_number = s.business_number OR fa.business_number IS NULL)
  AND fa.status = 'completed';

-- 문제 없으면 COMMIT, 잘못됐으면 ROLLBACK
COMMIT;
